
package aplicacion_lacteos;


public class Empleado {
    Cargo cargo;
    String nombre;
    String apellidos;
    String ci;
    String contacto;
    String direccion;

    public Empleado(Cargo cargo, String nombre, String apellidos, String ci, String contacto, String direccion) {
        this.cargo = cargo;
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.ci = ci;
        this.contacto = contacto;
        this.direccion = direccion;
    }
    
    
    
       
}
